    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            
            String sql = "select EmployeeID,UserName,Password,Type from USERS where (UserName =? and Password =? and Type =?)";
            int count = 0;
            
            prepstate=conn.prepareStatement(sql);
            int hashedPass=txt_password.getText().hashCode();
            prepstate.setString(1, txt_username.getText());
            prepstate.setString(2, hashedPass+"");
            prepstate.setString(3, txt_type.getSelectedItem().toString());
        
            resset =prepstate.executeQuery();
            while(resset.next())
            {
                int id=resset.getInt("EmployeeID");
                Employee.EmployeeID=id;
                String name=resset.getString("UserName");
                Employee.EmployeeName=name;
                count = count+1;
            }
            String access = (txt_type.getSelectedItem().toString());
            
            if(access == "Admin")
            {
                System.out.print(count);
                if(count == 1)
                {
                    JOptionPane.showMessageDialog(null, "Successful");
                    MainMenu MainMenu = new MainMenu();
                    MainMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    MainMenu.setVisible(true);
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect Username and/or Password.");
                }
            } 
            
            else if(access == "Management")
            { 
                System.out.print(count);
                if(count == 1)
                {
                    JOptionPane.showMessageDialog(null, "Successful");
                    MngrMainMenu managerMainMenu = new MngrMainMenu();
                    managerMainMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    managerMainMenu.setVisible(true);
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect Username and/or Password.");
                }
            }
            else if(access == "Employee")
            { 
                System.out.print(count);
                if(count == 1)
                {
                    JOptionPane.showMessageDialog(null, "Successful");
                    EmpMainMenu employeeMainMenu = new EmpMainMenu();
                    employeeMainMenu.setVisible(true);
                    employeeMainMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect Username and/or Password.");
                }
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally
        {
            try
            {
                resset.close();
                prepstate.close();
            }
            catch(Exception e)
            {
                
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

