    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String od="";
        String rn="";
        double late=0;
        double lwn=0;
        if(txt_deductions.getText().equals("0.0")==true && txt_reasons.getText().trim().isEmpty()==false)
        {
            JOptionPane.showMessageDialog(null, "Please fill the reasons for Deduction.");
        }
        else if(txt_deductions.getText().equals("0.0")==false && txt_reasons.getText().trim().isEmpty()==true)
        {
            JOptionPane.showMessageDialog(null, "Please fill the reasons for Deduction.");
        }
        else{
            try
            {
                String sql2="select * from Deduction where EmployeeID=?";
                prepstate=conn.prepareStatement(sql2);
                prepstate.setString(1, txt_employeeID.getText());
                resset=prepstate.executeQuery();

                if(resset.next())
                {
                    od=resset.getString("otherDeductions");
                    rn=resset.getString("Reason");
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
            finally
            {
                try
                {
                    resset.close();
                    prepstate.close();
                }
                catch(Exception e)
                {

                }
            }
            int present=0;
            int tlate=0;
            int leave=0;
            try
            {
                String sql3="select * from Attendance where EmployeeID=? order by AttendanceDate DESC";

                prepstate=conn.prepareStatement(sql3);
                prepstate.setString(1, txt_employeeID.getText());
                resset=prepstate.executeQuery();

                if(resset.next())
                {
                    if(resset.getInt("TotalLate")>5)
                    {
                        System.out.println(resset.getInt("TotalLate"));
                        Double salary=Double.parseDouble(txt_salary.getText());
                        late=0.01*salary;

                    }

                    present=resset.getInt("TotalPresent");
                    tlate=resset.getInt("TotalLate");
                    leave=resset.getInt("Leave");

                    if(resset.getInt("threshold")-(present+tlate+leave)>3)
                    {
                        lwn=0.02*(Double.parseDouble(txt_salary.getText()));
                    }
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
            finally
            {
                try
                {
                    resset.close();
                    prepstate.close();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }
            }

            try{

                if(txt_deductions.getText().equals("0.0")==false){
                    od=txt_deductions.getText()+";"+od;
                    rn=txt_reasons.getText()+";"+rn;
                }

                double total=0;
                StringTokenizer st=new StringTokenizer(od,";");
                while(st.hasMoreTokens())
                {
                    total=Double.parseDouble(st.nextToken())+total;
                }
                total=total+lwn+late;

                String sql="Update Deduction set LatePenalty='"+late+"',absence='"+lwn+"',otherDeductions='"+od+"', "
                + " Reason='"+rn+"' , TotalDeduction='"+(total+"")+"' where EmployeeID ='"+txt_employeeID.getText()+"'" ;

                prepstate=conn.prepareStatement(sql);
                prepstate.execute();

                // double total=Double.parseDouble(txt_overtimepay.getText()) + Double.parseDouble(txt_bonuses.getText());
                String totaldeductions=total+"";

                txt_totalDeduction.setText(totaldeductions);
                JOptionPane.showMessageDialog(null, "Deductions added Successfully");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
            finally
            {
                try
                {
                    resset.close();
                    prepstate.close();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

