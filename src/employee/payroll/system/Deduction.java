    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        try{

            String sql ="select * from Employee where EmployeeID =? ";

            prepstate=conn.prepareStatement(sql);
            prepstate.setString(1,txt_search.getText());
            resset=prepstate.executeQuery();

            if(resset.next()){
                String fn =resset.getString("FirstName");
                txt_firstname.setText(fn);

                String ln =resset.getString("LastName");
                txt_lastname.setText(ln);

                String empID =resset.getString("EmployeeID");
                txt_employeeID.setText(empID);

                String email =resset.getString("Email");
                txt_email.setText(email);

                String des =resset.getString("Designation");
                txt_designation.setText(des);

                String sal =resset.getString("Salary");
                txt_salary.setText(sal);

                String wr =resset.getString("Wage");
                txt_wagerate.setText(wr);

            }
            else
            {

                txt_firstname.setText("");

                txt_lastname.setText("");

                txt_employeeID.setText("");

                txt_wagerate.setText("");

                txt_designation.setText("");

                txt_salary.setText("");

                txt_totalDeduction.setText("");

                txt_email.setText("");

            }
        }
        catch(Exception e){

        }
        finally {

            try{
                resset.close();
                prepstate.close();
            }
            catch(Exception e){

            }
        }
        try{

            String sql1 ="select * from Deduction where EmployeeID =? ";

            prepstate=conn.prepareStatement(sql1);
            prepstate.setString(1,txt_search.getText());
            resset=prepstate.executeQuery();

            if(resset.next()){
                String lp= resset.getString("LatePenalty");
                txt_latepen.setText(lp);

                String abs= resset.getString("absence");
                txt_absence.setText(abs);

            }
            else
            {

                txt_latepen.setText("0.0");

                txt_absence.setText("0.0");

                txt_deductions.setText("0.0");

                txt_reasons.setText("");

            }

        }catch(Exception e){

        }
        finally {

            try{
                resset.close();
                prepstate.close();
            }
            catch(Exception e){

            }
        }
    }//GEN-LAST:event_txt_searchKeyReleased

